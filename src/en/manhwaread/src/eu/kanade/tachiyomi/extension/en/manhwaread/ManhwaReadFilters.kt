    ) {
    companion object {
        val options = mapOf(
            "Release" to "release",
            "New" to "new",
            "A-Z" to "alphabet",
            "Rating" to "rating",
            "Bookmarks" to "bookmarks",
            "Chapters" to "chapters",
            "Comments" to "comments",
            "Daily Views" to "daily_top",
            "Weekly Views" to "weekly_top",
            "Monthly Views" to "monthly_top",
            "Yearly Views" to "yearly_top",
            "All Time Views" to "all_top",
        )

        val POPULAR = FilterList(
            SortByFilter().apply {
                state = Selection(8, false)
            },
        )
        val LATEST = FilterList(
            SortByFilter().apply {
                state = Selection(0, false)
            },
        )
    }

    val queryName: String = "sortby"
    val queryValue: String get() = options[values[state?.index ?: 0]]!!
    val orderValue: String get() = if (state?.ascending == true) "asc" else "desc"
}

