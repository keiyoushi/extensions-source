package eu.kanade.tachiyomi.extension.ru.tomilolib

import eu.kanade.tachiyomi.source.model.Filter

open class SelectFilter(
    name: String,
    private val options: Array<Pair<String, String>>,
) : Filter.Select<String>(name, options.map { it.second }.toTypedArray()) {
    fun selected(): String? = options[state].first.ifBlank { null }
}

class TypeFilter : SelectFilter("Тип", TYPES)
class StatusFilter : SelectFilter("Статус", STATUSES)
class SortFilter : Filter.Sort("Сортировка", SORT_LABELS, Filter.Sort.Selection(0, false)) {
    fun selectedValue(): String = SORT_VALUES[state?.index ?: 0]
    fun ascending(): Boolean = state?.ascending ?: false
}

class GenreCheckBox(name: String) : Filter.CheckBox(name)
class GenreFilter : Filter.Group<GenreCheckBox>("Жанры", GENRES.map { GenreCheckBox(it) })

